package Jungol;

public class Jungol572 {
	public static int plus(int n1, int n2) {
		int r = n1 + n2;
		return r;
	}

	public static int minus(int p1, int p2) {
		int q = p1 - p2;
		return q;
	}

	public static void main(String[] args) {
		int res1 = plus(3, 4);
		int res2 = minus(5, 3);
		int res3 = minus(9, 3);
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);

	}

}
